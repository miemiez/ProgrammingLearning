/*
    ^：用于匹配字符串的开头

    $：用于匹配字符串的结尾

    []：范围
        [a-z]：小写字母、[0-9]：数字、[A-Z]：大写字母。

    {}：位数
        /^[a-z]{3}$/ 表示要三位都是字母，多了不行，少了也不行。

    ()分组：
        可用作分割字符串，把一个字符串分割成几个部分
*/
let reg = /^(\d{4})-(\d{1,2})-(\d{1,2})$/;
let str01 = "2023-06-09";
let result = reg.exec(str01);
console.log(result);//['2023-06-09','2023','06','09',index: 0,input: '2023-06-09',groups: undefined]
console.log(result[0]);//2023-06-09