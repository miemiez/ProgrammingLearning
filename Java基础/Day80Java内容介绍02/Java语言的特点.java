package Day80Java内容介绍02;

public class Java语言的特点 {
	/*
	 * 1.Java语言是面向对象的（oop）。
	 * 2.Java语言是健壮的。
	 *     Java的强类型机制，异常处理，垃圾的自动收集等是Java程序健壮性的重要保证
	 * 3.Java语言是跨平台性的。
	 *     即：一个编译好的.class文件可以在多个系统下运行，这张特性被称为跨平台
	 * 4.Java语言是解释型的。
	 *     解释型语言：javascript，PHP，java
	 *     编译型语言：c/c++
	 *     区别是：解释性语言，编译后的代码，不能直接被机器执行，需要解释器来执行；而编译型语言，编译后的代码，可以直接被机器执行。
	 */
}
