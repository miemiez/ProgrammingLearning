public class Test {
	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		int c = 29;
		int d = 100;
		System.out.println("a + b = " + (a + b));
		System.out.println("c - b = " + (c - b));
		System.out.println("a * b = " + (a * b));
		System.out.println("c / b = " + (c / b));//取整数
		System.out.println("c % b = " + (c % b));
		System.out.println("a++ = " + a++);
		System.out.println("a++ = " + a++);
		System.out.println("a-- = " + a--);
		System.out.println("++d = " + ++d);
		System.out.println("--d = " + --d);
		System.out.println(a);
	}
}