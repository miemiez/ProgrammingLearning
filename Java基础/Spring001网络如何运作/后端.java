public class 后端 {
	/*
	 * 后端有很多不同的部分，不同的网站或 Web 应用程序可能有截然不同的后端
	 *     1.网站或应用程序的前端由发送到客户端（如网络浏览器）的 HTML、CSS、JavaScript 和静态资产组成。
	       2.Web 服务器是在某处的计算机上运行的进程，它通过 Internet 侦听传入的信息请求并发回响应。
           3.存储、访问和操作数据是 Web 应用程序后端的很大一部分
           4.数据存储在数据库中，可以是关系数据库或 NoSQL 数据库。
           5.Web 应用程序的服务器端（有时称为应用程序服务器）处理授权和身份验证等重要任务。
           6.Web 应用程序的后端通常有一个 Web API，它是一种通过 HTTP 请求和响应与应用程序数据交互的方式。
           7.用于构建 Web 应用程序的前端和后端的技术被称为堆栈，许多不同的语言和框架可用于构建强大的后端。

	 */
}
