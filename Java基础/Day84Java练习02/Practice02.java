package Day84Java练习02;

public class Practice02 {
	public static void main(String[] args) {
		System.out.println(11/3);//3
		System.out.println(10/5);//2
		System.out.println(10%2);//0
		System.out.println(-10.5%3);//a % b当a是小数时，公式 = a - (int)a / b * b
		
		int i = 6;
		int c = 6;
		System.out.println(++i+i);//7+7
		System.out.println(c+++c);//6+7;
	}
}
