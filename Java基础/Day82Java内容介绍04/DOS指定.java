package Day82Java内容介绍04;

public class DOS指定 {
	/*
	 * 常用的dos命令：
	 *     1.查看当前目录是有什么
	 *         dir dir d:\abc2\test
	 *     2.切换到其他盘下：盘符号 cd
	 *         cd /D c:
	 *     3.切换到当前盘的其他目录下
	 *         绝对路径：cd d:\abc2\test
	 *         相对路径：cd ..\..\abc2\test
	 *     4.切换到上一级
	 *         cd ..
	 *     5.切换到根目录
	 *         cd \
	 *     6.查看指定的目录下所有的子级目录
	 *         tree d:
	 *     7.清屏
	 *         cls
	 *     8.退出DOS
	 *         exit
	 *     9.创建目录：md
	 *     10.删除目录：rd
	 *     11.拷贝文件：copy
	 *         例：copy hello.txt e:\ok.txt
	 *     12.删除文件：del
	 *     13.输入内容到文件：echo
	 *         例：echo hello > ok.txt
	 *     14.输入一个空文件：type
	 *         例：type nul > ok.txt
	 *     15.移动/剪切文件：move
	 *         例：move hello.txt f:\ok.txt
	 *         
	 * 配置环境变量：是为了在dos的任意目录可以去使用java和javac命令
	 *         
	 * 相对路径：从当前目录开始定位，形成的一个路径
	 * 绝对路径：从顶级目录d开始定位，形成的路径
	 */
}
