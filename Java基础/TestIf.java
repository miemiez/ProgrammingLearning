public class TestIf{
	public static void main(String[] args) {
		int x = 10;
		if(x < 10) {
			System.out.println("The If statement is executed here.");
		}else if(x == 10) {
			System.out.println("The Else if statement is executed here.");
		}else {
			System.out.println("The Else statement is executed here");
		}
	}
}