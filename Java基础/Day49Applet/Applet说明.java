package Day49Applet;

public class Applet说明 {
	/*
	 * （一）Java程序可分为Application和Applet两种类型。
	 *     Application程序就是带有main()方法的程序，又称之为Java应用程序。
	 *     Applet程序又被称之为小应用程序，是指能够在其他应用程序（通常是网页浏览器）中执行的Java程序，并不是指Applet程序就一定是一个代码量小的一个小程序
	 * 
	 * （二）Applet的运行
	 *     Applet程序需要嵌入到网页中运行，网页是由HTML语言编写的
	 *     Applet程序除了可以在浏览器中运行以外，也可以通过Java开发工具包（JDK）所自带的appletviewer程序来运行
	 *     
	 * （三）HTML与Applet
	 *     <applet>标记用于在网页中嵌入applet程序：
	 *     如：
	 *         <applet code="HelloJavaApplet" width=200 height=50></applet>  （将在网页中HelloJavaApplet这个类与applet相对应）
	 */
	
}
