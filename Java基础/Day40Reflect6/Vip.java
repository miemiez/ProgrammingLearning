package Day40Reflect6;

public class Vip {
	int no;
	String name;
	boolean sex;
	
	public Vip() {
		
	}
	
	public Vip(int no) {
		this.no = no;
	}
	
	public Vip(int no,String name) {
		this.no = no;
		this.name = name;
	}
	
	public Vip(int no,String name,boolean sex) {
		this.no = no;
		this.name = name;
		this.sex = sex;
	}
}
